package ssml;

public class SSMLWritingException extends Exception {

    public SSMLWritingException() {
    }

    public SSMLWritingException(String message) {
        super(message);
    }

    public SSMLWritingException(Throwable cause) {
        super(cause);
    }

    public SSMLWritingException(String message, Throwable cause) {
        super(message, cause);
    }
    
}
